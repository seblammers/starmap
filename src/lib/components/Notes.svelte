<h2 id="notes">Notes</h2>

<ol>
  <li>
    This map does not contain all stars of the night sky. It's the ~5,000 stars
    that most people would be able to see when they look up at night without
    using professional star gazing equipment.
    <a href="#n1">&#x2934;</a>
  </li>
  <li>
    It was not possible to exactly match each and every distinct amount of light
    years to an historic event. Instead, for each star the distance in light
    years was mapped to the <em>closest</em> event in the list of options.
    However, the timeline has more entries the closer it gets to the current
    day. That means that stars that are close to us (small amount of light
    years) will be matched closer to an exact date. On the flipside, stars that
    are very far away will be matched less close to the exact amount of light
    years.
    <a href="#n2">&#x2934;</a>
  </li>
</ol>
